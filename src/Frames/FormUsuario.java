    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         if (!(jNombre.getText().equals("") || jApellido.getText().equals("") || jUsuario.getText().equals("") || jContrasena.getText().equals("") || jTipo.getText().equals("") )&& Integer.parseInt(jTipo.getText())>=1&& Integer.parseInt(jTipo.getText())<=3) {

             usuarios objA = new usuarios();
            objA.setNombre(jNombre.getText().trim());
            objA.setApellido(jApellido.getText().trim());
            objA.setUsuario(jUsuario.getText().trim());
            objA.setContrasena(jContrasena.getText().trim());
            objA.setTipo_usuario(Integer.parseInt(jTipo.getText().trim()));
            if (new IngUsuarios().Guardar(objA)) {
                JOptionPane.showMessageDialog(null, "Ingresado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "NO ingresado");
            }
            
        }else JOptionPane.showMessageDialog(null, "Ingrese datos correctos");
         vaciar();
    }//GEN-LAST:event_jButton1ActionPerformed

