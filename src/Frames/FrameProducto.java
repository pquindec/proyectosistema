    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jNombre.getText().equals("")||jNprecio.getText().equals("")||jDprecio.getText().equals("")||jCantidad.getValue().equals("")){
            Producto pro = new Producto();
            pro.setNombre(jNombre.getText().trim());
            pro.setNprecio(Double.parseDouble(jNprecio.getText().trim()));
            pro.setDprecio(Double.parseDouble(jDprecio.getText().trim()));
            pro.setCantidad((Integer) jCantidad.getValue());
            pro.setTipo((String) jTipo.getSelectedItem());
            if (new IngProducto().Guardar(pro)) {
                JOptionPane.showMessageDialog(null, "Ingresado correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "NO ingresado");
            }
        }else JOptionPane.showMessageDialog(null, "Ingrese datos correctos");
        vaciar();
    }//GEN-LAST:event_jButton1ActionPerformed

