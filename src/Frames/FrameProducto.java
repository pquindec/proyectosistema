    private void jNombreKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jNombreKeyPressed
        // TODO add your handling code here:
        char c=evt.getKeyChar();
          if(!Character.isLetter(c)) {
              getToolkit().beep();
              
              evt.consume();
              
              JOptionPane.showMessageDialog(null,"Ingrese solo letras");
              jNombre.setText("");
          }
    }//GEN-LAST:event_jNombreKeyPressed

    private void jNprecioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jNprecioKeyPressed
        // TODO add your handling code here:
        char c=evt.getKeyChar();
          if(!Character.isDigit(c)&&Character.isLetterOrDigit(c)) {
              getToolkit().beep();
              
              evt.consume();
              
              JOptionPane.showMessageDialog(null,"Ingrese solo numeros");
              jNprecio.setText("");
          }
    }//GEN-LAST:event_jNprecioKeyPressed

